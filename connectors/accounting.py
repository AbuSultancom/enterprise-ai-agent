@dataclass
class SchemaConfig:
    """Loaded from config/accounting_schema.json — table/column mappings."""
    version: int = 1
    name: str = "Onyx Pro"
    tables: dict = field(default_factory=lambda: DEFAULT_SCHEMA["tables"])
    query_aliases: dict[str, str] = field(default_factory=dict)

    @classmethod
    def load(cls, path: str = SCHEMA_CONFIG_PATH) -> "SchemaConfig":
        if not os.path.exists(path):
            return cls()
        try:
            with open(path, encoding="utf-8") as f:
                data = json.load(f)
            # Merge with defaults so new keys are always present
            tables = dict(DEFAULT_SCHEMA["tables"])
            for key, val in data.get("tables", {}).items():
                if key in tables:
                    tables[key].update(val)
                else:
                    tables[key] = val
            return cls(
                version=data.get("version", 1),
                name=data.get("name", "Custom"),
                tables=tables,
                query_aliases=data.get("query_aliases", {}),
            )
        except (json.JSONDecodeError, OSError):
            return cls()

    def save(self, path: str = SCHEMA_CONFIG_PATH) -> None:
        os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
        # Strip alias from saved config (it's auto-derived)
        clean = {"version": self.version, "name": self.name,
                 "tables": {k: {kk: vv for kk, vv in v.items() if kk != "alias"}
                            for k, v in self.tables.items()},
                 "query_aliases": self.query_aliases}
        with open(path, "w", encoding="utf-8") as f:
            json.dump(clean, f, indent=2, ensure_ascii=False)

    def render_query(self, template: str) -> str:
        """Replace {table.X.col} / {table.alias} / {table.table} placeholders."""
        result = template
        for tb_name, tb in self.tables.items():
            alias = tb.get("alias", tb_name[:1])
            result = result.replace(f"{{{tb_name}.alias}}", alias)
            result = result.replace(f"{{{tb_name}.table}}", tb["table"])
            for col_key, col_val in tb.get("columns", {}).items():
                result = result.replace(f"{{{tb_name}.col.{col_key}}}", col_val)
        return result

    def get_resolved_queries(self) -> dict[str, str]:
        return {k: self.render_query(v) for k, v in QUERY_TEMPLATES.items()}


