def discover_schema(db_url: str) -> SchemaConfig:
    """
    Connect to the database and try to identify the schema automatically.
    Returns a SchemaConfig with matched table/column names.
    """
    if not HAS_SQLALCHEMY:
        raise RuntimeError("SQLAlchemy not installed. Run: pip install sqlalchemy pyodbc")

    engine = create_engine(db_url)
    inspector = inspect(engine)
    all_tables = inspector.get_table_names()

    schema = SchemaConfig(version=1, name="Discovered")

    for tb_key, heuristic in DISCOVERY_HEURISTICS.items():
        # Find best matching table
        best_table = _best_match(all_tables, heuristic["keywords"])
        if not best_table:
            continue

        cols = inspector.get_columns(best_table)
        col_names = [c["name"] for c in cols]

        # Map columns
        mapped_cols: dict[str, str] = {}
        for col_key, candidates in heuristic["columns"].items():
            match = _best_match(col_names, candidates)
            if match:
                mapped_cols[col_key] = match

        # Must have at least an id
        if not mapped_cols:
            continue

        schema.tables[tb_key] = {
            "table": best_table,
            "alias": tb_key[:1],
            "columns": mapped_cols,
        }

    return schema


def _best_match(names: list[str], keywords: list[str]) -> str | None:
    """Return the name that best matches one of the keywords (case-insensitive, substring)."""
    name_lower = [(n, n.lower()) for n in names]

    # 1. Exact match
    for name, nl in name_lower:
        if nl in keywords:
            return name

    # 2. Substring match (keyword in name)
    best = None
    best_score = 0
    for name, nl in name_lower:
        for kw in keywords:
            if kw in nl:
                score = len(kw)
                if score > best_score:
                    best_score = score
                    best = name

    if best:
        return best

    # 3. Partial word match
    for name, nl in name_lower:
        for kw in keywords:
            # Check if any word in name matches
            name_words = set(re.split(r"[\s_]", nl))
            kw_words = set(re.split(r"[\s_]", kw))
            if name_words & kw_words:
                return name

    return None


# ─── Connector ────────────────────────────────────────────────────
class AccountingConnector:
    """Runs whitelisted, read-only queries against the accounting database."""

    def __init__(self, db_url: str | None = None, schema: SchemaConfig | None = None):
        self.db_url = db_url or os.getenv("ACCOUNTING_DB_URL", "")
        self.schema = schema or SchemaConfig.load()
        self._engine: Engine | None = None

    @property
    def available(self) -> bool:
        return bool(self.db_url) and HAS_SQLALCHEMY

    @property
    def engine(self) -> Engine:
        if self._engine is None:
            self._engine = create_engine(self.db_url, pool_size=3, pool_recycle=1800)
        return self._engine

    @property
    def resolved_queries(self) -> dict[str, str]:
        return self.schema.get_resolved_queries()

    def test_connection(self) -> dict:
        """Test database connection and return status info."""
        if not self.db_url:
            return {"connected": False, "error": "No database URL configured"}
        if not HAS_SQLALCHEMY:
            return {"connected": False, "error": "SQLAlchemy not installed"}
        try:
            with self.engine.connect() as conn:
                conn.execute(text("SELECT 1"))
            return {"connected": True, "database": str(self.engine.url).split("@")[-1]}
        except Exception as e:
            return {"connected": False, "error": str(e)[:200]}

    def get_schema_info(self) -> dict:
        """Return current schema mapping for display/debug."""
        return {
            "name": self.schema.name,
            "version": self.schema.version,
            "tables": {
                k: {"table": v["table"], "columns": list(v.get("columns", {}).keys())}
                for k, v in self.schema.tables.items()
            },
            "queries": list(self.resolved_queries.keys()),
        }

    def run(self, query_name: str, **params: Any) -> list[dict[str, Any]]:
        if not self.available:
            raise RuntimeError(
                "Accounting database is not configured. "
                "Set ACCOUNTING_DB_URL in .env and ensure SQLAlchemy + pyodbc are installed."
            )
        queries = self.resolved_queries
        sql = queries.get(query_name)
        if sql is None:
            raise ValueError(f"Unknown query '{query_name}'. Available: {list(queries)}")
        if not READONLY_PATTERN.match(sql):
            raise PermissionError("Only read-only (SELECT) queries are allowed.")
        if HAS_SEMICOLON.search(sql):
            raise PermissionError("Multi-statement queries are not allowed.")
        try:
            with self.engine.connect() as conn:
                result = conn.execute(text(sql), params)
                return [dict(row) for row in result.mappings()]
        except Exception as e:
            raise RuntimeError(f"Query '{query_name}' failed: {e}")


