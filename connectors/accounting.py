def discover_schema(db_url: str) -> SchemaConfig:
    """
    Connect to the database and try to identify the schema automatically.
    Returns a SchemaConfig with matched table/column names.
    """
    if not HAS_SQLALCHEMY:
        raise RuntimeError("SQLAlchemy not installed. Run: pip install sqlalchemy pyodbc")

    engine = create_engine(db_url)
    inspector = inspect(engine)
    all_tables = inspector.get_table_names()

    schema = SchemaConfig(version=1, name="Discovered", db_url=db_url)

    for tb_key, heuristic in DISCOVERY_HEURISTICS.items():
        # Find best matching table
        best_table = _best_match(all_tables, heuristic["keywords"])
        if not best_table:
            continue

        cols = inspector.get_columns(best_table)
        col_names = [c["name"] for c in cols]

        # Map columns
        mapped_cols: dict[str, str] = {}
        for col_key, candidates in heuristic["columns"].items():
            match = _best_match(col_names, candidates)
            if match:
                mapped_cols[col_key] = match

        # Must have at least an id
        if not mapped_cols:
            continue

        schema.tables[tb_key] = {
            "table": best_table,
            "alias": tb_key[:1],
            "columns": mapped_cols,
        }

    return schema


def _best_match(names: list[str], keywords: list[str]) -> str | None:
    """Return the name that best matches one of the keywords (case-insensitive, substring)."""
    name_lower = [(n, n.lower()) for n in names]

    # 1. Exact match
    for name, nl in name_lower:
        if nl in keywords:
            return name

    # 2. Substring match (keyword in name)
    best = None
    best_score = 0
    for name, nl in name_lower:
        for kw in keywords:
            if kw in nl:
                score = len(kw)
                if score > best_score:
                    best_score = score
                    best = name

    if best:
        return best

    # 3. Partial word match
    for name, nl in name_lower:
        for kw in keywords:
            name_words = set(re.split(r"[\s_]", nl))
            kw_words = set(re.split(r"[\s_]", kw))
            if name_words & kw_words:
                return name

    return None


# ─── Connector ────────────────────────────────────────────────────
class AccountingConnector:
    """
    Runs whitelisted, read-only queries against MULTIPLE accounting databases.

    Usage:
        connector = AccountingConnector()
        connector.list_databases()      # list configured DBs
        connector.run("onyx", "sales_summary", start="2024-01-01", end="2024-12-31")
        connector.run("inventory", "item_summary")  # another DB

    Backward compatible: methods without a db_name argument default to
    the first configured database.
    """

    def __init__(self):
        self._databases: dict[str, SchemaConfig] = _load_multi_db_config()
        self._engines: dict[str, Engine] = {}

    # ─── Properties ───────────────────────────────────────────

    @property
    def available(self) -> bool:
        """True if at least one database is configured and has a URL."""
        return any(
            s.db_url and HAS_SQLALCHEMY
            for s in self._databases.values()
        )

    @property
    def default_db(self) -> str | None:
        """Return the key of the first enabled database, or None."""
        for key, schema in self._databases.items():
            if schema.enabled and schema.db_url:
                return key
        for key, schema in self._databases.items():
            if schema.db_url:
                return key
        return next(iter(self._databases)) if self._databases else None

    # ─── Database management ──────────────────────────────────

    def list_databases(self) -> list[dict]:
        """Return info about all configured databases."""
        result = []
        for db_key, schema in self._databases.items():
            engine_key = f"{db_key}"
            engine = self._engines.get(engine_key)
            connected = engine is not None
            result.append({
                "key": db_key,
                "name": schema.name,
                "enabled": schema.enabled,
                "has_url": bool(schema.db_url),
                "connected": connected,
                "table_count": len(schema.tables),
                "query_count": len(schema.get_resolved_queries()),
            })
        return result

    def add_database(self, db_key: str, name: str, db_url: str,
                     schema: SchemaConfig | None = None) -> SchemaConfig:
        """
        Add a new database to the config. Returns the SchemaConfig.
        If schema is None, uses the default Onyx Pro schema.
        """
        if db_key in self._databases:
            raise ValueError(f"Database key '{db_key}' already exists")

        schema = schema or SchemaConfig(
            version=1, name=name, db_url=db_url, enabled=True
        )
        schema.db_url = db_url
        schema.name = name
        self._databases[db_key] = schema
        _save_multi_db_config(self._databases)
        return schema

    def remove_database(self, db_key: str) -> bool:
        """Remove a database from the config. Returns True if removed."""
        if db_key not in self._databases:
            return False
        del self._databases[db_key]
        self._engines.pop(f"{db_key}", None)
        _save_multi_db_config(self._databases)
        return True

    def _get_schema(self, db_name: str | None = None) -> SchemaConfig:
        """Resolve schema for a db_name. Falls back to default DB."""
        key = db_name or self.default_db
        if key is None:
            raise RuntimeError(
                "No accounting database is configured. "
                "Add one via setup or set ACCOUNTING_DB_URL in .env."
            )
        schema = self._databases.get(key)
        if schema is None:
            raise ValueError(
                f"Database '{key}' not found. Available: {list(self._databases)}"
            )
        return schema

    def _get_engine(self, db_name: str | None = None) -> Engine:
        """Get or create an engine for the given database."""
        key = db_name or self.default_db
        if key is None:
            raise RuntimeError(
                "No accounting database is configured. "
                "Set ACCOUNTING_DB_URL in .env and ensure SQLAlchemy + pyodbc are installed."
            )
        schema = self._get_schema(key)
        if not schema.db_url:
            raise RuntimeError(
                f"Database '{key}' has no db_url configured."
            )
        if not HAS_SQLALCHEMY:
            raise RuntimeError(
                "SQLAlchemy not installed. "
                "For SQL Server: pip install sqlalchemy pyodbc\n"
                "For Oracle: pip install sqlalchemy oracledb"
            )

        engine_key = f"{key}"
        if engine_key not in self._engines:
            self._engines[engine_key] = create_engine(
                schema.db_url, pool_size=2, pool_recycle=1800
            )
        return self._engines[engine_key]

    # ─── Test / Schema info ───────────────────────────────────

    def test_connection(self, db_name: str | None = None) -> dict:
        """Test database connection and return status info."""
        key = db_name or self.default_db
        if key is None:
            return {"key": None, "connected": False, "error": "No database configured"}
        schema = self._get_schema(key)
        if not schema.db_url:
            return {"key": key, "name": schema.name, "connected": False,
                    "error": "No database URL configured"}
        if not HAS_SQLALCHEMY:
            return {"key": key, "name": schema.name, "connected": False,
                    "error": "SQLAlchemy not installed"}
        try:
            engine = self._get_engine(key)
            with engine.connect() as conn:
                conn.execute(text("SELECT 1"))
            host = str(engine.url).split("@")[-1] if "@" in str(engine.url) else str(engine.url)
            return {"key": key, "name": schema.name, "connected": True,
                    "database": host}
        except Exception as e:
            return {"key": key, "name": schema.name, "connected": False,
                    "error": str(e)[:200]}

    def get_schema_info(self, db_name: str | None = None) -> dict:
        """Return current schema mapping for display/debug."""
        schema = self._get_schema(db_name)
        key = db_name or self.default_db
        return {
            "key": key,
            "name": schema.name,
            "version": schema.version,
            "enabled": schema.enabled,
            "has_url": bool(schema.db_url),
            "tables": {
                k: {"table": v["table"], "columns": list(v.get("columns", {}).keys())}
                for k, v in schema.tables.items()
            },
            "queries": list(schema.get_resolved_queries().keys()),
        }

    @property
    def resolved_queries(self) -> dict[str, str]:
        """Return resolved queries for the default database."""
        schema = self._get_schema(None)
        return schema.get_resolved_queries()

    # ─── Query execution ──────────────────────────────────────

    def run(self, query_name: str, db_name: str | None = None, **params: Any) -> list[dict[str, Any]]:
        """
        Run a whitelisted read-only query on the specified database.

        Args:
            query_name: name of the query template to use
            db_name: database key (or None for default)
            **params: query parameters

        Returns: list of dict rows
        """
        key = db_name or self.default_db
        schema = self._get_schema(key)
        engine = self._get_engine(key)

        queries = schema.get_resolved_queries()
        sql = queries.get(query_name)
        if sql is None:
            raise ValueError(
                f"Unknown query '{query_name}' for database '{key}'. "
                f"Available: {list(queries)}"
            )
        if not READONLY_PATTERN.match(sql):
            raise PermissionError("Only read-only (SELECT) queries are allowed.")
        if HAS_SEMICOLON.search(sql):
            raise PermissionError("Multi-statement queries are not allowed.")
        try:
            with engine.connect() as conn:
                result = conn.execute(text(sql), params)
                return [dict(row) for row in result.mappings()]
        except Exception as e:
            raise RuntimeError(f"Query '{query_name}' on '{key}' failed: {e}")

