"""
Accounting connector — read-only access to ERP/accounting databases.

Supports MULTIPLE databases (Onyx Pro, Inventory, HR, etc.) managed
in a single schema config file at config/accounting_schema.json.

Designed for **Onyx Pro** (YemenSoft) but works with any SQL-based ERP.
Table/column names are NOT hardcoded — loaded from a schema config file
generated by the discovery tool or written manually.

SECURITY: enforces read-only queries (SELECT/WITH only).
Use a dedicated DB user with SELECT-only permissions in production.

Backward compatible: if ACCOUNTING_DB_URL env var is set without a
config file, or the config file uses the legacy v1 format, the
connector auto-migrates to the new multi-DB format.
"""
