def _load_multi_db_config() -> dict[str, SchemaConfig]:
    """
    Load the schema config file and return a dict of {db_key: SchemaConfig}.

    Backward-compatible with v1 (legacy single-DB format).
    If the config file doesn't exist, checks the ACCOUNTING_DB_URL env var
    for a fallback single DB.
    """
    path = SCHEMA_CONFIG_PATH

    # Case 1: No config file exists — fallback to env var
    if not os.path.exists(path):
        return _fallback_from_env()

    try:
        with open(path, encoding="utf-8") as f:
            data = json.load(f)
    except (json.JSONDecodeError, OSError):
        return _fallback_from_env()

    version = data.get("version", 1)

    # Case 2: Legacy v1 format (single database)
    if version == 1 or "databases" not in data:
        return _migrate_v1(data)

    # Case 3: v2+ multi-DB format
    databases: dict[str, SchemaConfig] = {}
    for db_key, db_data in data.get("databases", {}).items():
        databases[db_key] = SchemaConfig.from_dict(db_data)

    return databases


def _fallback_from_env() -> dict[str, SchemaConfig]:
    """Create a single 'onyx' DB from ACCOUNTING_DB_URL env var."""
    db_url = os.getenv("ACCOUNTING_DB_URL", "")
    if db_url:
        return {
            "onyx": SchemaConfig(
                version=1,
                name="Onyx Pro (from env)",
                db_url=db_url,
                enabled=True,
            )
        }
    return {
        "onyx": SchemaConfig(
            version=1,
            name="Onyx Pro (default)",
            db_url="",
            enabled=False,
        )
    }


def _migrate_v1(data: dict) -> dict[str, SchemaConfig]:
    """Migrate a v1 (legacy single-DB) config to multi-DB format."""
    # Use ACCOUNTING_DB_URL from env if not in config
    db_url = data.get("db_url", "") or os.getenv("ACCOUNTING_DB_URL", "")

    # Build tables with defaults
    tables = dict(DEFAULT_SCHEMA["tables"])
    for key, val in data.get("tables", {}).items():
        if key in tables:
            tables[key].update(val)
        else:
            tables[key] = val

    schema = SchemaConfig(
        version=1,
        name=data.get("name", "Onyx Pro"),
        db_url=db_url,
        enabled=True,
        tables=tables,
        query_aliases=data.get("query_aliases", {}),
    )
    return {"onyx": schema}


def _save_multi_db_config(databases: dict[str, SchemaConfig]) -> None:
    """Save the multi-DB config to disk."""
    db_dict = {}
    for db_key, schema in databases.items():
        db_dict[db_key] = schema.to_dict()

    config = {
        "version": 2,
        "databases": db_dict,
    }
    path = SCHEMA_CONFIG_PATH
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(config, f, indent=2, ensure_ascii=False)


